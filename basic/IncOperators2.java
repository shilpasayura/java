class IncOperators2{
   public static void main(String[] args){
	   int a, b , c, d, e, f;
	   a=2;b=3;
	   c=a*a*b;
	   System.out.println(c);
	   
	   d=++a*a*b;
	   System.out.println(d);
	   b=a++;
	   e=a*a*b;
	   System.out.println(d);
	   b=++a;
	   f=a*a*b;
	   System.out.println(f);
	   
    }
}
