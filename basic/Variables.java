public class Variables {
  public static void main(String[] args) {
	  
   char myLetter = 'A';
   System.out.println("char " + myLetter);

   String name = "Ama";
   System.out.println("String " + name);

   byte b=127;
   System.out.println("byte " + b);
   
   int Num = 1576;
   System.out.println("int " + Num);
   
   long LargeNum = 1500020202020L;
   System.out.println("long " + LargeNum);

   float height = 5.99f;
   System.out.println("float " + height);

   boolean female= true;
   System.out.println("boolean " + female);
  }
}


